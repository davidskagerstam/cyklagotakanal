	<div class="mappop" id="sjotorpPop"> <h3>Sjötorp</h3>
    <Button class="NEXT" id="lyrestadNEXT" style="top: 0px; right: 0px;"><p>Lyrestad 7.0km >></p></Button>
    <p>This is the western harbour of the «Göta Canal», at the lake «Vänern». Here the freighters that operated on the canal were build and repaired. Today Sjötorp is a cosy village were you can stroll among the old warehouses and sheds and still get the feeling of harbour. During the season a lot of people visit this place and its locks, restaurants and shops. Be sure to pay a visit to the «Göta canal» museum!</p>
    </div>
    
	<div class="mappop" id="lyrestadPop"><Button class="PREV" id="sjotorpPREV" style="top: 0px; left: 0px;"><p><< Sjötorp 7.0km</p></Button> <h3>Lyrestad</h3>
	<Button class="NEXT" id="norrkvarnNEXT" style="top: 0px; right: 0px;"><p>Norrkvarn 2.0km >></p></Button>
	<p>In the old days, Lyrestad was a reloading site for goods shipped on the canal. The local farmers would come here with their crop so that it could be shipped and sold elsewhere. This is a good example for how the canal opened up the inland for trade and development. Nowadays, the old warehouse in the guest harbour has been rebuild into a museum of local history. Here you will also find cosy cafés and the storied food store «Farbror Frejs».</p>
	</div>
	
	<div class="mappop" id="norrkvarnPop"><Button class="PREV" id="lyrestadPREV" style="top: 0px; left: 0px;"><p><< Lyrestad 2.0km</p></Button><h3>Norrkvarn</h3>
	<Button class="NEXT" id="hajstorpNEXT" style="top: 0px; right: 0px;"><p>Hajstorp 5.2km >></p></Button>
	<p> «Norrqvarn» offers a lot for the whole family. Next to the hotel a miniature canal has been build that invites children to playfully explore the canal by themselves. At the restaurant many local specialties are offered that can be enjoyed on the wharf at sunset. The lock is only a few yards away from the restaurant. Here starts a nature path whre beautiful nature blends with the traces left from the construction of the canal. 

</p><p><a href="/sv/paket">«Norrqvarn» is part part of some of our tour packages. You can find more information about those here.</a></p>
	</div>
	
	<div class="mappop" id="hajstorpPop"> 
	<Button class="PREV" id="norrkvarnPREV" style="top: 0px; left: 0px;"><p><< Norrkvarn 5.2km</p></Button> <h3>Hajstorp</h3>
	<Button class="NEXT" id="torebodaNEXT" style="top: 0px; right: 0px;"><p>Töreboda 4.4km >></p></Button>
	<p> Even though Hajstorp is a tiny village alongside the «Göta Canal» it is very much alive. Many people are fascinated by how the locks are operated, lifting and lowering boats several feet only with the help of the water. Here in Hajstorp one can observe the locking of passing boats close up in the little garden of the café just a few yards from the lock and boats. Have a cup of coffee, a slice of cake or some lighter lunch while observing this operation. The café uses only organic ingredients for the making of their dishes. 
	
	<br><br>The old warehouse in Hajstorp has been converted into a museum with an exhibition showing the live of a farmer for 100 ago. The exhibition is made to be appealing for children as well. Also crafts from local artists are shown here. Every year a meeting for vintage automobiles is organised in Hajstorp. This meeting is very popular with several hundret vintage cars participating. Please feel free to ask us about current events in Hajstorp.</p>
	</div>
	
	<div class="mappop" id="torebodaPop" style="visibility: visible;">
	<Button class="PREV" id="hajstorpPREV" style="top: 0px; left: 0px; visibility: visible;"><p><< Hajstorp 4.4km</p></Button> <h3>Töreboda</h3>
	<Button class="NEXT" id="jonsbodaNEXT" style="top: 0px; right: 0px; visibility: visible;"><p>Jonsboda 6km >></p></Button>
	<p>
	Töreboda is situated in the middle of «Göta Canal's» western route. Here the railway crosses the canal, which makes is easily accessible both by car and by train. Several cafés and restaurants can be found close by the guest harbour and alongside with the canal runs a promenade with sitting benches in different designs. Each bench has been donated by local businesses. During summer nights almost every day live music is played here. Our bicycle rentals at the café «Visthuset» can be found here aswell.</p>
	
	</div>
	
	<div class="mappop" id="jonsbodaPop"> 
	<Button class="PREV" id="torebodaPREV" style="top: 0px; left: 0px; "><p><< Töreboda 6km</p></Button> <h3>Jonsboda</h3>
	<Button class="NEXT" id="vassbackenNEXT" style="top: 0px; right: 0px;"><p>Vassbacken 4.8km >></p></Button>
	
		<p>If you travel south from Töreboda, the first bit of the way will lead you through a dense alley of trees running alongside pastures with sheep and cattle. After a bit you will reach Jonsboda. During the days when the «Göta Canal» still was used as an important freight route for various goods, Jonsboda was a reloading site. Today a little guest harbour can be found at this site. The little cottage that used to be the home for the bridge-keeper has been redone and is a café now. On some evenings live music will be played here. Please feel free to ask us about current events in Jonsboda.</p>
	</div>
	
	<div class="mappop" id="vassbackenPop"> <Button class="PREV" id="jonsbodaPREV" style="top: 0px; left: 0px; "><p><< Jonsboda 4.8km</p></Button><h3>Vassbacken</h3>
	<Button class="NEXT" id="lanthojdenNEXT" style="top: 0px; right: 0px;"><p>Lanthöjden 2km >></p></Button>
	<p>Until 1864, a post office could be found in Vassbacken. The little cottage, which originally stood a little further away from the water, was moved closer to the canal and houses now a little museum were the postal history of the area is shown.
<br><br>
In Vassbacken you will also find a little guest harbour, one of «Göta Canal's» most beautiful camping sites and a little café, were you can both enjoy a cup of coffee or a snack. Next to the camping you can also find a miniature golf course. During the summer at some evenings, live music is played on the little stage next to the water, which usually attracts many visitors.</p>
	</div>
	
	<div class="mappop" id="lanthojdenPop"> <Button class="PREV" id="vassbackenPREV" style="top: 0px; left: 0px; "><p><< Vassbacken 2km</p></Button><h3>Lanthöjden</h3>
	<Button class="NEXT" id="tatorpNEXT" style="top: 0px; right: 0px;"><p>Tåtorp 3km >></p></Button>
	<p>A stately obelisk marks the highest spot of land of «Göta Canal's» western route. Originally, a wooden monument of king Karl XIII stood here as a memory, since it was under his reign the decision was made to build the canal. 
	<br><br>During the 1930s the canal got a new route past Lanthöjden. When the canal was first build, sections passing terrain with bedrock were avoided. Later it was decided to blast a new route through the bedrock to straighten out the route passing Lanthöjden a bit. A memory board is therefore risen at Lanthöjden. Between the old and the new route of the canal an island emerged were a little watchmen's cottage was build. Since both routes were very narrow, it was the task of the watchman to signal the boats if they were allowed to pass or if the needed to wait for other boats to pass first.
<br><br>
At Lanthöjden the agricultural landscape that was dominating the character of the canal this far is now replaced by woodlands and rocky hillsides.</p>
	</div>
	
	<div class="mappop" id="tatorpPop"> <Button class="PREV" id="lanthojdenPREV" style="top: 0px; left: 0px; "><p><< Lanthöjden 3km</p></Button><h3>Tåtorp</h3>
	<Button class="NEXT" id="forsvikNEXT" style="top: 0px; right: 0px;"><p>Forsvik 30km >></p></Button>
	<p>The cosy village Tåtorp sits right at the lake «Viken». If you come here by boat you will need to pass the last remaining hand operated lock of the western route of the canal, before you are free to sail out on the lake «Viken», which is the highest point of the canal itself. Right after the lock, a dutch drawbridge opens to let the boats pass by. 
	<br><br>Today, the semaphore that was previously used at Lanthöjden to regulate the boat traffic can be found in Tåtorp. The hostel in Tåtorp, an organic business, sometimes hosts art exhibitions. If you rather would like to enjoy the beautiful nature you can rent a kayak or a boat and the lake, which is rich in fish, invites for some quiet moments with the fishing rod.
<br><br>
<a href="/sv/paket">Some of our tour packages have Tåtorp as a destination. You can read more about those here.</a></p>
	</div>
	
	<div class="mappop" id="forsvikPop"> <Button class="PREV" id="tatorpPREV" style="top: 0px; left: 0px; "><p><< Tåtorp 30km</p></Button><h3>Forsvik</h3>
	<Button class="NEXT" id="karlsborgNEXT" style="top: 0px; right: 0px;"><p>Karlsborg 10km >></p></Button>
	
	<p>At the other end of the lake «Viken», the former industrial town of Forsvik is situated, which has its roots in the 13th century. It was here that the building of the canal started and here still stands the first lock of the canal. The old cast iron drawbridge is the first of its kind in Sweden, but today it is held open at all times. Right in the center of the town the old industry is preserved, set beautifully right next to the rapids. Parts of the old industry were in business up until the 1970s. Here the «Viken» flows into the lake «Bottensjön».
	<br><br>Today, different exhibitions - both permanent and temporary- and guided tours meditate all the different aspects of the live in the industrial town through the centuries. Guest exhibitions can be found here aswell. Different theme days throughout the season offer a lot of activities for children. During the summer the popular vintage car meetings are organised in Forsvik, offering live music, food and activities for children wich makes them appealing for the whole family. The little café at the waterfront has opened during the summer and here one can peacefully enjoy a cup of coffee or some ice cream. The lock in Forsvik is the last one boats have to pass before being able to reach the lake «Vättern» by passing through the lake «Bottensjön». </p>
	</div>
	
	<div class="mappop" id="karlsborgPop"><Button class="PREV" id="forsvikPREV" style="top: 0px; left: 0px; "><p><< Forsvik 10km</p></Button> <h3>Karlsborg</h3>
	<p>Karlsborg is situated between the two lakes «Bottensjön» and «Vättern». The last bit of the «Göta Canal's» western route stretches right through the town center. Karlsborg is a quite young community and emerged after the fortress was build at the last turn of the century. This town was originally planned to function as the reserve capital of Sweden. The military imprints the town even today as several military units still are stationed here and the dog handlers of the Swedish police are educated here. 
	<br><br>There is a lot to see and do here in Karlsborg. It is therefore a good idea to plan in an extra day or two, so that all the sights can be explored in peace. The old fortress is a given point to visit! During the whole year exhibitions, guided tours, markets and other events are organised here. Also the the very imposing garrison church should be payed a visit. 
	<br><br>But Karlsborg can also offer several cafés, small boutiques, miniature golf, fishing and beautiful bathing spots. If you visit by boat, you have two guest harbours to choose from. For those who would like to enjoy nature, it is not far to several hiking routes. You can chose from short and easy routs or longer routs stretching through rough terrain.</p>
