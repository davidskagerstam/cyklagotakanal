	<Button class="NEXT" id="forsvikNEXT" style="top: 0px; right: 0px;"><p>Forsvik 30km >></p></Button>
	<p>Genau am See Viken liegt das gemütliche Örtchen Tåtorp. Wer auf dem Kanal entlangfährt, muss hier die einzige handbetriebene Schleuse des westlichen Götakanals passieren, bevor man auf den See Viken fahren kann. Der See ist der höchste Punkt des Kanals. Genau nach der Schleuse öffnet sich eine holländische Zugbrücke, um den Bootsverkehr vorbei zulassen. </p>
	
	<p>In Tåtorp steht heutzutage die Semafore, die früher bei Lanthöjden verwendet wurde, um den Bootsverkehr zu regeln. Das Gästehaus in Tåtorp, das stark auf Ökologisch orientiert ist, beherbergt manchmal Kunstausstellungen. Wer lieber die naturschöne Lage genießen möchte, kann sich ein Kajak oder ein Boot mieten. Die fischreichen Gewässer des Viken laden auf ruhige Momente mit der Angel ein. </p>
	<p><a href="/de/Erlebnispakete">Tåtorp ist ein Zielort mancher unserer Erlebnispakete. Mehr Information über diese finden Sie hier.</a></p>
	</div>
	
	<div class="mappop" id="forsvikPop"> <Button class="PREV" id="tatorpPREV" style="top: 0px; left: 0px; "><p><< Tåtorp 30km</p></Button><h3>Forsvik</h3>
	<Button class="NEXT" id="karlsborgNEXT" style="top: 0px; right: 0px;"><p>Karlsborg 10km >></p></Button>
	<p>Auf der andere Seite des Sees Viken liegt der kleine, ehemalige Industrieort Forsvik, dessen Geschichte bis ins 13. Jahrhundert zurück reicht. Forsvik war einer der Orte, in dem die ersten Spatenstiche für den Bau des Götakanals getätigt wurden. Hier findet man auch die erste Schleuse des Götakanal. Die alte gusseiserne Zugbrücke ist die erste ihrer Art in Schweden. </p>
	
	<p>Zentral im Ort ist das alte Industriegelände erhalten, das sehr hübsch genau an den Stromschnellen liegt. An dieser Stelle fließt der See Viken in den See Bottensjön. Noch bis in die siebziger Jahre des 20. Jahrhunderts wurden Teile der Industrieanlage betrieben. Verschiedene Ausstellungen, sowohl permanente als auch kurzzeitige und geführte Touren vermitteln die verschiedenen Aspekte vom Leben in dem Industrieörtchen durch die Jahrhunderte. Man findet auch thematisch andere Gastausstellungen in den Räumen der alten Industrie. Verschiedene Thematage in der Sommersaisong richten sich gezielt an die jüngeren Besucher. </p>
	
	<p>Jeden Sommer werden in Forsvik mehrere Oldtimertreffen veranstaltet, die mit Musik, Speis und Trank und Aktiviteten für Kinder die ganze Familie einladen. In dem kleinen Café, das in den Sommermonaten geöffnet hat, kann man in ganz entspannt genau am Wasser eine Tasse Kaffee oder ein Eis genießen. Die Schleuse in Forsvik ist die letzte die man passieren muß, bevor man über den Bottensjö in den Vättern gelangt.</p>
	</div>
	
	<div class="mappop" id="karlsborgPop"><Button class="PREV" id="forsvikPREV" style="top: 0px; left: 0px; "><p><< Forsvik 10km</p></Button> <h3>Karlsborg</h3>
	<p>Zwischen dem Bottensjö und dem Vättern liegt Karlsborg. Die letzte Streckung der westlichen Route des Kanals verläuft hier genau durch das Ortszentrum. Karlsborg ist ein relativ junger Ort und wuchs nach dem Bau der Festung zur vorigen Jahrhundertwende hervor. Ursprünglich war Karlsborg als Reservehauptstadt geplant und noch heute wird der Ort vom Militär geprägt. So ist Karlsborg noch heute der Standort verschiedener Militärverbände und die Hundeführer der Polizei werden hier ausgebildet.</p>
	
	<p>In Karlsborg kann man vieles sehen und erleben. Es lohnt sich hier einen extra Tag einzuplanen, um sich alles in Ruhe angucken zu können. Die alte Festung, die genau im Zentrum liegt ist ein gegebenes Besuchsziel. Das ganze Jahr über wird man hier mit Ausstellungen, geführten Touren, Aktiviteten, Märkten und anderen Events gelockt. </p>
	
	<p>Weiterhin gibt es in Karlsborg viele kleine Cafées und Lädchen, man kann angeln, Minigolf spielen oder baden gehen. Wer mit dem Boot angereist kommt, kann zwischen zwei Gästehäfen wählen und wer lieber raus in die Natur möchte hat es nicht weit zu mehreren Wanderrouten. Man hat hier die Auswahl zwischen kurzen und leichten Strecken oder kann sich für längere Strecken in schwererem Gelände entscheiden. </p>
