	<div class="mappop" id="vassbackenPop"> <Button class="PREV" id="jonsbodaPREV" style="top: 0px; left: 0px; "><p><< Jonsboda 4.8km</p></Button><h3>Vassbacken</h3>
	<Button class="NEXT" id="lanthojdenNEXT" style="top: 0px; right: 0px;"><p>Lanthöjden 2km >></p></Button>
	<p>Bis 1864 gab es in Vassbacken eine Poststation. Das kleine Posthäuschen, das ursprünglich ein Stück weiter vom Wasser weg lag, wurde etwas näher an den Kanal gestellt. Heute beherbergt es ein kleines Heimatkundemuseum, das die Postgeschichte um den Götakanal beleuchtet.</p>
	
	<p>In Vassbacken findet man außerdem einen kleinen Gästehafen, einen der schönsten Campingplätze des Götakanal und ein Café, wo man sowohl einen Kaffee trinken oder auch einen leichten Imbiss genießen kann. Genau am Camping gibt es auch eine kleine Minigolfbahn. An Sommerabenden wird hier oft Livemusik auf der kleinen Bühne, die direkt an der Kanalkante steht, gespielt oder zu anderen Unterhaltungsprogrammen eingeladen. Diese Veranstaltungen locken oftmals viele Besucher.</p>
	</div>
	
	<div class="mappop" id="lanthojdenPop"> <Button class="PREV" id="vassbackenPREV" style="top: 0px; left: 0px; "><p><< Vassbacken 2km</p></Button><h3>Lanthöjden</h3>
	<Button class="NEXT" id="tatorpNEXT" style="top: 0px; right: 0px;"><p>Tåtorp 3km >></p></Button>
	<p>An dem höchsten Landpunkt der westlichen Route des Götakanal steht ein stattlicher Obelisk. Ursprünglich stand hier ein Monument aus Holz, auf dem Karl XIII abgebildet war. Dieses Monument wurde zur Erinnerung daran aufgestellt, dass unter der Regierung dieses Königs der Beschluß über den Kanalbau verabschiedet worden war. </p>
	
	<p>In den dreißiger Jahren des 20. Jahrhunderts bekam der Götakanal bei Lanthöjden eine neue Streckung. Eine Gedenktafel erinnert hier daran. Beim ursprünglichen Bau des Kanals wollte man Streckungen vermeiden, die durch zu steiniges Gelände führten. Später beschloß man sich jedoch eine neue Streckung zu sprengen, um den Kanal bei Lanthöjden etwas zu begradigen. Zwischen der alten und der neuen Route bildete sich eine Insel, auf der ein Wächterhäuschen gebaut wurde. Sowohl die alte als auch die neue Streckung des Kanals sind an dieser Stelle sehr eng. Der Wächter hatte als Aufgabe dem Schiffsverkehr zu signalisieren, ob dieser passieren durfte oder auf andere Kähne warten musste.</p>
	
	<p>Bei Lanthöjden wird die bis jetzt dominierende Agrarlandschaft von Wald und Felsen abgelöst.</p>
