                <h2>Vårweekend längs Göta kanal</h2>
                <p> 
                    Göta kanal är vackert om våren! Precis när träden börjar spricka ut i grönt och allt börjar grönska och blomma. Så varför inte skämma bort sig själv lite med en weekend längs Göta kanal. I detta cykelpaket får du både skön motion i vacker natur, vällagade menyer och skön avkoppling.
                </p>
                <p>
                    Dag 1 - Töreboda -Sjötorp - Norrqvarn (27 km)
Du hämtar ut cykeln på Café Visthuset i Töreboda, här får du också med dig en lunchpicknick att avnjuta under dagen. Du cyklar förbi slussar i vacker natur där knopparna precis har brustit ut i ljusgrönt och har hela tiden nedförsbacke på vägen till Sjötorp. Denna ort är Göta kanals hamn mot Vänern och förr byggdes och renoverades båtar här. I Sjötorp finns också ett museum om kanalen, värt ett besök. På Norrqvarn Hotell och Konferens kommer sedan middagen att avnjutas i form av en trerätters lokalproducerad Smaka på Västsverige-meny, det är också här du kommer bo under natten.
                </p>
                <p>
                    Dag 2 - Norrqvarn - Töreboda (13 km)
Efter söndagens frukostbuffé cyklar du i egen takt tillbaka till Töreboda. Här kan du bland annat besöka Sveriges minsta färja Lina. På Café Visthuset väntar sedan en smarrig smörgåsbricka med lokala specialiteter till lunch. Du kan sedan välja att cykla vidare en tur på eftermiddagen eller lämna tillbaka cyklarna efter lunchen.
                </p>
                 <p>Paketet finns enbart helgerna 14-15 maj samt 28-29 maj.</p>
                
